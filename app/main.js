const { app, BrowserWindow } = require('electron');

app.whenReady().then(() => {
    createWindow()
})

function createWindow () {
    const win = new BrowserWindow({
        width: 800,
        height: 600,
        autoHideMenuBar: true,
        resizable: false
    })
  
    win.loadFile('app/gui.html');
}